const HAT_LINES = {
  none:      '',
  crown:     '   \\^^^/    ',
  tophat:    '   [___]    ',
  propeller: '    -+-     ',
  halo:      '   (   )    ',
  wizard:    '    /^\\     ',
  beanie:    '   (___)    ',
  tinyduck:  '    ,>      ',
};

export function renderSprite(bones, frame = 0) {
  const frames = BODIES[bones.species];
  if (!frames) return ['  [?????]  '];
  const body = frames[frame % frames.length].map(line =>
    line.replaceAll('{E}', bones.eye),
  );
  const lines = [...body];
  if (bones.hat !== 'none' && !lines[0].trim()) {
    lines[0] = HAT_LINES[bones.hat] || lines[0];
  }
  if (!lines[0].trim() && frames.every(f => !f[0].trim())) lines.shift();
  return lines;
}

export function spriteFrameCount(species) {
  return BODIES[species]?.length ?? 1;
}

export function renderFace(bones) {
  const e = bones.eye;
  const faces = {
    duck:     `(${e}>`,      goose:    `(${e}>`,
    blob:     `(${e}${e})`,  cat:      `=${e}\u03c9${e}=`,
    dragon:   `<${e}~${e}>`, octopus:  `~(${e}${e})~`,
    owl:      `(${e})(${e})`,penguin:  `(${e}>)`,
    turtle:   `[${e}_${e}]`, snail:    `${e}(@)`,
    ghost:    `/${e}${e}\\`,  axolotl:  `}${e}.${e}{`,
    capybara: `(${e}oo${e})`,cactus:   `|${e}  ${e}|`,
    robot:    `[${e}${e}]`,  rabbit:   `(${e}..${e})`,
    mushroom: `|${e}  ${e}|`,chonk:    `(${e}.${e})`,
  };
  return faces[bones.species] || `(${e}${e})`;
}
